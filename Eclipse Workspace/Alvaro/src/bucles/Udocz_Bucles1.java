package bucles;

public class Udocz_Bucles1 {

	public static void main(String[] args) {

		/*
		 * Programa que imprima todos los números pares hasta el 100 (este incluido)
		 */

		// Mensaje inicial
		System.out.println("Aqui están todos los números pares hasta el 100 incluido:");

		// Bucle for que muestra los números pares hasta el 100 incluido
		for (int i = 2; i <= 100; i += 2) {
			System.out.println(i);
		}

	}

}
