package Pruebas;
//Probar estructura condicional
public class PruebaIf {
	public static void main(String args[]) {
		int num=4;
		
		//Condicional simple
		if(num<0) {
			System.out.println("Es negativo");
		}
		//Condicional doble
		if(num<0) {
			System.out.println("Es negativo");
		}else {
			System.out.println("Es positivo");
		}
	}
}
