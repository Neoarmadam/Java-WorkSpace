package Actividades_UT4;
/*
 * El metodo <pow> calcula la potencia del número que
 * se indique elevandolo al valor que se indique en el exponente.
 */
public class Actividad_6 {

	public static void main(String args[]){
		double base =5;
		double exponente = 2;
		double potencia;
		potencia = Math.pow(base, exponente);
		System.out.println("la potencia es " +potencia);
	}
}
